export const porgressPercentage = (number) => {
  if (number === "80") {
    return `after:content-['80%']`;
  }
  if (number === "67") {
    return `after:content-['67%']`;
  }
  if (number === "90") {
    return `after:content-['90%']`;
  }
  if (number === "35") {
    return `after:content-['35%']`;
  }
};
