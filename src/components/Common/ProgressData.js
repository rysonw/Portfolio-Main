export const porgressPercentage = (number) => {
  if (number === "50") {
    return `after:content-['50%']`;
  }
  if (number === "76") {
    return `after:content-['76%']`;
  }
  if (number === "91") {
    return `after:content-['91%']`;
  }
  if (number === "59") {
    return `after:content-['59%']`;
  }
};
